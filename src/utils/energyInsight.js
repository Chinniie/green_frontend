export const getEnergyDescription = (totalKwh) => {
  if (totalKwh < 20) {
    return "การใช้ไฟของคุณอยู่ในระดับต่ำ 👍 ประหยัดพลังงานได้ดี";
  }

  if (totalKwh < 50) {
    return "การใช้ไฟอยู่ในระดับปกติ ⚖️";
  }

  if (totalKwh < 80) {
    return "คุณใช้ไฟค่อนข้างสูง ⚠️ อาจมีอุปกรณ์ใช้ไฟหนัก";
  }

  return "การใช้ไฟสูงมาก 🔥 ควรตรวจสอบอุปกรณ์ เช่น แอร์ หรือเครื่องใช้ไฟฟ้าขนาดใหญ่";
};